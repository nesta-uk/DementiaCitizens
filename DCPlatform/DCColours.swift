import Foundation
import UIKit

extension UIColor {

  class func dementiaCitizensBackground() -> UIColor {
    return UIColor(red: 253/255, green: 253/255, blue: 245/255, alpha: 1)
  }

  class func dementiaCitizensBlue() -> UIColor {
    return UIColor(red: 37/255, green: 108/255, blue: 110/255, alpha: 1)
  }

  class func dementiaCitizensBlack() -> UIColor {
    return UIColor(red: 83/255, green: 73/255, blue: 71/255, alpha: 1)
  }

  class func dementiaCitizensLightGrey() -> UIColor {
    return UIColor(red: 202/255, green: 202/255, blue: 196/255, alpha: 1)
  }

  class func dementiaCitizensDarkBackground() -> UIColor {
    return UIColor(red: 218/255, green: 218/255, blue: 213/255, alpha: 1)
  }

}
